int	ft_sqrt(int nb)
{
	
	long int	x;
	int			x_1;

	x_1 = 0;
	if (nb <= 0)
		return (0);
	x = 1;
	while (x != x_1)
	{
		x_1 = x;
		x = (x + nb / x) / 2;
		if (x == 1 + x_1)
			break;
	}
	if (nb == x * x)
		return (x);
	else
		return (0);
}
