#include<stdio.h>

int	ft_str_is_printable(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str >= ' ' && *str <= '~')
	{
		if (*(str + 1) == '\0')
			return (1);
		str++;
	}
	return (0);
}
