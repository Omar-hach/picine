char	*ft_strupcase(char *str)
{
	int	count;

	count = 0;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
		}
		str++;
		count++;
	}
	return (str - count);
}
