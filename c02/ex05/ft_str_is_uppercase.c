#include<stdio.h>

int	ft_str_is_uppercase(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str >= 'A' && *str <= 'Z')
	{
		if (*(str + 1) == '\0')
		{
			return (1);
		}
		str++;
	}
	return (0);
}
